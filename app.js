// This is my current age in human years
let myAge = 25;

// These are the early years of life, subject to change
let earlyYears = 2;

earlyYears = earlyYears * 10.5;

// My current age minus the early years equal laterYears, subject to change
let laterYears = myAge - 2;

// To calculate my age in dog years, I multiplied my later years times 4.
laterYears = laterYears * 4

// by adding my earlyYears and laterYears together, we get my age in dog years:
myAgeInDogYears = earlyYears + laterYears;

const myName = 'Ali'.toLowerCase();

console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);